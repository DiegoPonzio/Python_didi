def main():
    ventana = Tk()
    ventana.title("Calculadora-RBEE")
    ventana.columnconfigure(0, weight=1)
    ventana.rowconfigure(0, weight=1)
    ventana.resizable(0,0)
    app = Interfaz(ventana)
    ventana.mainloop()

main()