        self.boton9 = ttk.Button(self.frame, text="9", command=lambda: operacion.click(9))
        self.boton9.grid(column=2, row=4)
        self.botonM = ttk.Button(self.frame, text="x", command=lambda: operacion.click("*"))
        self.botonM.grid(column=3, row=4)
        self.boton4 = ttk.Button(self.frame, text="4", command=lambda: operacion.click(4))
        self.boton4.grid(column=0, row=5)
        self.boton5 = ttk.Button(self.frame, text="5", command=lambda: operacion.click(5))
        self.boton5.grid(column=1, row=5)
